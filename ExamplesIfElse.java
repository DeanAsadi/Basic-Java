package day1.examples;

public class ExamplesIfElse {
	public static void main (String[] arg) {
		int x = 9;
		int y = 10;
		
		if( x > y ) {
			System.out.println("X is greater then Y");
		} else if ( x < y ) {
			System.out.println("X is less then Y");
		} else {
			System.out.println("X is equal to Y");
		}
	}
	

}
